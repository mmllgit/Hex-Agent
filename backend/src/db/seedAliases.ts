async function seedAliases() {
  await connectDB();

  let updated = 0;
  for (const [name, aliases] of Object.entries(ALIAS_MAP)) {
    const result = await Champion.updateOne(
      { name },
      { $addToSet: { aliases: { $each: aliases } } }
    );
    if (result.modifiedCount > 0) updated++;
  }

  console.log(`Updated aliases for ${updated} champions`);
  await disconnectDB();
}

seedAliases().catch((err) => {
  console.error("Seed aliases failed:", err);
  process.exit(1);
});
